                       // Increment & Decrement

#include<iostream>
using namespace std;
int main()
{
    int a=10;
    int b=a++;                     //post increment
    cout<<a<<endl<<b<<endl;

    int c=10;
    int d=++c;                       //pre increment
    cout<<c<<endl<<d<<endl;

    int e=10;
    int f=e--;                         // post decrement
    cout<<e<<endl<<f<<endl;

    int g=10;
    int h=--g;                      // pre decrement
    cout<<g<<endl<<h<<endl;



}